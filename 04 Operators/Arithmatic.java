package demo;

public class Arithmatic {
	public static void main(String[] args) {

		int a = 50;
		float b = 25.5f;
		
		System.out.println(a+b);	
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);

//		--------------------------------------
		
		int p = 0;
		int q = 25;
		
		System.out.println(p+q);	
		System.out.println(p-q);
		System.out.println(p*q);
		System.out.println(p/q);
		System.out.println(p%q);


	}
}