package demo;

public class Comparison {
	

	public static void main(String[] args) {

		int a = 50;
		float b = 60;
		
		System.out.println(a>b);	
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a!=b);

}
}